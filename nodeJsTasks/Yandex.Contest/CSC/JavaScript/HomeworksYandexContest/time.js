function gcd(a, b) {
    if (b) {
        return gcd(b, a % b);
    } else {
        return Math.abs(a);
    }
}

function phi(n) {
    let answer = n;
    for (let i = 0; i < p.length; i++) {
        if (p[i] > n)
            break;
        if (n % p[i] === 0) {
            answer -= answer / p[i];
        }
    }
    return answer;
}


